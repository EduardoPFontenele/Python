"Media Aritmetica"
"Escreva um programa que solicite tres numeros ao usuario, calcule a media aritmetica e exiba o resultado"
n1 = int(input("Digite um numero:"))
n2 = int(input("Digite outro numero:"))
n3 = int(input("Digite um numero mais uma vez:"))
resultado = (n1+n2+n3)/3

print(f"-"*60)
print(f"Dados os numeros {n1},{n2},{n3}. A medía aritmética é {resultado}")
print(f"-"*60)
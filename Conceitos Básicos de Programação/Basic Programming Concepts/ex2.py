"Conversão de temperatura"
"Escreva um programa que solicite uma temperatura em graus Celsius e a converta para Fahrenheit"
Celsius = float(input("Digite uma temperatura em ºC:"))
Fahrenheit = (Celsius * 9/5) + 32

print(f"-----------------")
print(f"{Celsius}ºC é igual à {Fahrenheit} ºF")
print(f"-----------------")
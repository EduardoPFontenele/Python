"Escreva um algoritmo que, utilizando um vetor de inteiros, com capacidade de 5 valores, conte e"
"armazene os valores de 1 até 5."

def contador():

    VETOR = [] #Criando um vetor de n espaços

    for i in range (1,6): #Para numeros num alcance de 1 a 5
        VETOR.append(i) #Guardar os numeros dentro do vetor

    print(VETOR) #Mostrar vetor

if __name__ == "__contador__":
    contador()

def main():
    contador()
if __name__ == "__main__":
    main()
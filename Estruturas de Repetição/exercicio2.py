def impares():

    numeros = 1 #Inicializando a variavel numeros com 1

    while numeros < 20: #Enquanto numeros forem menores que 20
        print(numeros) #Mostra numeros
        numeros += 2 #numeros recebe +2 a cada rotação do programa
        
if __name__ == "__impares__":
    impares()

def main():
    impares()
if __name__ == "__main__":
    main()
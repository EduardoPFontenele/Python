valor = int(input("Informe um numero inteiro:"))#Usuario irá informar um valor inteiro qualquer

if valor % 2 == 0: #se o resto da divisão por 2 por igual à 0
    print(f"{valor} é par!")#mostrar na tela que o valor informado pelo usuário é par
else:#caso a condição não se cumpra
    print(f"{valor} é ímpar!")#informar que o valor informado pelo usuario é impar